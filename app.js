    saveState() {
        // Save current language preference
        localStorage.setItem('kbapp_language', this.currentLanguage);
    }

    loadState() {
        // Load saved language preference
        const savedLang = localStorage.getItem('kbapp_language');
        if (savedLang) {
            this.switchLanguage(savedLang);
        }
    }
